CREATE TABLE MEETTING_DOC (
  MEET_SEQ INTEGER AUTO_INCREMENT PRIMARY KEY,  MEET_DT varchar(10) NOT NULL,  MEET_TITLE varchar(1000) NOT NULL,  MEET_CONTENTS text NOT NULL,  REG_DT date NOT NULL,  UPT_DT date NOT NULL,  LOCK_YN char(1) NOT NULL default 'N'
);
CREATE TABLE CHOIR_STATUS (
  STATUS_CD char(1)  NOT NULL,
  STATUS_NM varchar(20) NOT NULL,
  USE_YN char(1) NOT NULL default 'Y',  ORDERBY_NO int(11) NOT NULL,  PRIMARY KEY  (STATUS_CD)
);
CREATE TABLE CHOIR_PRACTICE_INFO (
  PRACTICE_DT varchar(10) NOT NULL default '',  PRACTICE_CD varchar(10) NOT NULL default '',  MUSIC_INFO text,  ETC_MSG text,  LOCK_YN char(1) NOT NULL default 'N',  PRIMARY KEY  (PRACTICE_DT,PRACTICE_CD)
);
CREATE TABLE CHOIR_PRACTICE (PRACTICE_CD varchar(10) NOT NULL,  PRACTICE_NM varchar(30) NOT NULL,  USE_YN char(1) NOT NULL default 'Y',  ORDERBY_NO int(11) NOT NULL);
CREATE TABLE CHOIR_POSITION (POSITION_CD varchar(10) NOT NULL,  POSITION_NM varchar(20) NOT NULL,  USE_YN char(1) NOT NULL default 'Y',  ORDERBY_NO int(11) NOT NULL,  PRIMARY KEY  (POSITION_CD));
CREATE TABLE CHOIR_PART (PART_CD varchar(10) NOT NULL default '',  PART_ENG_NM varchar(20) NOT NULL,  PART_NM varchar(20) NOT NULL,  USE_YN char(1) NOT NULL default 'Y',  ORDERBY_NO int(11) NOT NULL,  PRIMARY KEY  (PART_CD));
CREATE TABLE CHOIR_MEMBER (MEMBER_ID INTEGER AUTO_INCREMENT PRIMARY KEY,  MEMBER_NM varchar(20) NOT NULL,  BIRTHDAY varchar(20) default NULL,  BIRTHDAY_CD char(1) default NULL,  PHONE_NO varchar(13) default NULL,  E_MAIL varchar(50) default NULL,  ADDRESS text,  PART_CD varchar(10) NOT NULL,  POSITION_CD varchar(10) NOT NULL,  C_POSITION_CD varchar(20) default NULL,  STATUS_CD char(1) NOT NULL,  ETC_MSG text,  REG_DT date default NULL,  MODIFY_DT date default NULL);
CREATE TABLE CHOIR_C_POSITION (
  C_POSITION_CD varchar(10) NOT NULL default '',
  C_POSITION_NM varchar(20) NOT NULL,
  USE_YN char(1) NOT NULL default 'Y',
  ORDERBY_NO int(11) NOT NULL,
  PRIMARY KEY  (C_POSITION_CD)
);
CREATE TABLE CHOIR_ATTENDANCE (PRACTICE_DT VARCHAR(10) NOT NULL , PRACTICE_CD VARCHAR(2) NOT NULL , MEMBER_ID INTEGER NOT NULL , PRIMARY KEY (PRACTICE_DT, PRACTICE_CD, MEMBER_ID));
CREATE INDEX idx_USE_YN on CHOIR_PART(USE_YN);
CREATE INDEX idx_CHOIR_POSITION_USE_YN  on CHOIR_POSITION(USE_YN);
CREATE INDEX IDX_STATUS_CD on CHOIR_MEMBER(STATUS_CD);
CREATE INDEX IDX_POSITION_CD on CHOIR_MEMBER(POSITION_CD);
CREATE INDEX IDX_PART_CD on CHOIR_MEMBER(PART_CD);
CREATE INDEX IDX_MEMBER_PART_3 ON CHOIR_MEMBER (PART_CD ASC, POSITION_CD ASC, C_POSITION_CD ASC, STATUS_CD ASC);
CREATE INDEX IDX_MEMBER_PART_2 ON CHOIR_MEMBER (PART_CD ASC, POSITION_CD ASC, STATUS_CD ASC);
CREATE INDEX IDX_MEMBER_PART_1 ON CHOIR_MEMBER (PART_CD ASC, STATUS_CD ASC);
COMMIT;

